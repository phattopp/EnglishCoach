// Language Switcher Logic
function setLanguage(lang) {
    localStorage.setItem('lang', lang);
    const elements = document.querySelectorAll('[data-key]');

    // Update active button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.innerText.toLowerCase() === lang) {
            btn.classList.add('active');
        }
    });

    // Update text content
    elements.forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            element.innerText = translations[lang][key];
        } else {
            // Optional fall back or keep existing
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Mobile Menu Logic
const hamburger = document.querySelector(".hamburger");
const navLinks = document.querySelector(".nav-links");
const links = document.querySelectorAll(".nav-links li a");

hamburger.addEventListener("click", () => {
    hamburger.classList.toggle("active");
    navLinks.classList.toggle("active");
});

// Close menu when clicking a link
links.forEach(link => {
    link.addEventListener("click", () => {
        hamburger.classList.remove("active");
        navLinks.classList.remove("active");
    });
});

// Initialize Language on Load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'de';
    setLanguage(savedLang);

    // Form Handling
    const form = document.getElementById('contact-form');
    if (form) {
        form.addEventListener('submit', async (e) => {
            e.preventDefault();
            const formData = new FormData(form);
            const data = Object.fromEntries(formData);

            const submitBtn = form.querySelector('button[type="submit"]');
            const originalText = submitBtn.innerText;
            submitBtn.disabled = true;
            submitBtn.innerText = translations[document.documentElement.lang].id === 'de' ? 'Senden...' : 'Sending...';

            try {
                // In a real scenario, this would POST to /api/submit
                // For now, we simulate a delay and success
                // await fetch('/api/submit', { method: 'POST', body: JSON.stringify(data) });

                await new Promise(resolve => setTimeout(resolve, 1000)); // Simulate network request

                // Show success
                form.style.display = 'none';
                document.getElementById('form-success').style.display = 'block';
                document.querySelector('.contact-heading').style.display = 'none';

            } catch (error) {
                console.error('Error:', error);
                document.getElementById('form-error').style.display = 'block';
                submitBtn.disabled = false;
                submitBtn.innerText = originalText;
            }
        });
    }

    // Hero Slider Logic
    const slides = document.querySelectorAll('.hero-slider .slide');
    if (slides.length > 0) {
        let currentSlide = 0;
        const slideInterval = 2000; // Change every 2 seconds

        function nextSlide() {
            slides[currentSlide].classList.remove('active');
            currentSlide = (currentSlide + 1) % slides.length;
            slides[currentSlide].classList.add('active');
        }

        setInterval(nextSlide, slideInterval);
    }
});
